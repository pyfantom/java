public class HolaMundo {
    public static void main(String[] args) {
        // Eerst "Hello World!" =>message => afdrukken
        String message = "Hallo world!";
        System.out.println(message);

        // Wijziging messagenaar "Hola Mundo!" en afdrukken
        message = "Holla Mundo!";
        System.out.println(message);
    }
}
